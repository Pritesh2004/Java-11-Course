package basic.core;

public class DataTypes {
    public static void main(String[] args) {

//        Representing datatypes using students information;

        int studentAge= 20;                   //Primitive Data Type
        char studentFirstInitial = 'P';       //Primitive Data Type
        char studentLastInitial = 'P';        //Primitive Data Type
        double studentCgpa = 8.9;             //Primitive Data Type
        boolean hasPerfectAttendance = true;  //Primitive Data Type

        String studentName = "Pritesh Patil"; //Reference Data Type

        System.out.println("Name : " + studentName);
        System.out.println("First Initial : " + studentFirstInitial);
        System.out.println("Last Initial : " + studentLastInitial);
        System.out.println("Age : " + studentAge);
        System.out.println("CGPA : " + studentCgpa);
        System.out.println("Perfect Attendance : " + hasPerfectAttendance);


    }
}
